#include "../ft_printf.h"

int			ptr_is_flag(char c)
{
	return (c == '+' || c == '-' || c == '0'\
	|| c == '#' || c == ' ') ? (1) : (0);
}

int			ptr_is_length(char c)
{
	return (c == 'h' || c == 'l' || c == 'j' || c == 'z') ? (1) : (0);
}

int			ptr_str_is_num(char c)
{
	return (c >= 48 && c <= 57) ? (1) : (0);
}
